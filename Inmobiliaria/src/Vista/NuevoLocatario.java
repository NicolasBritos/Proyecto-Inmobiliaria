    private void btn_agregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregar1ActionPerformed
         if ((txt_dni.getText().isEmpty() || txt_apellido.getText().isEmpty() || txt_nombre.getText().isEmpty() || txt_estadoCivil.getText().isEmpty()  || txt_fecha.getDate()== null || txt_email.getText().isEmpty()  ||  txt_telefono.getText().isEmpty()  || txt_imagen.getText().isEmpty() )
              ||( txt_calle.getText().isEmpty() || txt_numero.getText().isEmpty() || txt_piso.getText().isEmpty() || txt_barrio.getText().isEmpty()  || txt_localidad.getText().isEmpty() || txt_codigoPostal.getText().isEmpty() )){    
                      JOptionPane.showMessageDialog(null, "Todos los campos son OBLIGATORIOS");
         }else{
////////////Obtiene datos de la persona
        int dni = parseInt(txt_dni.getText());
        byte[] comprobante = null;
        String tipoDocumento = (String) cmb_tipoDocumento.getSelectedItem();
        String apellido = txt_apellido.getText();
        String nombre = txt_nombre.getText();
        String estadoCivil = txt_estadoCivil.getText();
        Date fechaNacimiento = txt_fecha.getDate();
        String email = txt_email.getText();
        String telefono = txt_telefono.getText();
        String tipoActividad = (String) cmb_tipoActividad.getSelectedItem();
        String usuario = txt_usuario.getText();
        String password = txt_password.getText();
        ///Obtiene el archivo imagen de la ruta obtenida en el txt_imagen
        if (!"".equals(txt_imagen.getText())){
           comprobante = miMenu.controlador.ObtenerImagen(txt_imagen.getText());
        }
        /////////////Obtiene datos de la Direccion
        String calle= txt_calle.getText();
        String numero = txt_numero.getText();
        int piso = parseInt(txt_piso.getText());
        String barrio = txt_barrio.getText();
        String localidad = txt_localidad.getText();
        int codigoPostal = parseInt(txt_codigoPostal.getText());
        ////////////Llama al Controlador para Almacenar los datos en la BD.
        try {
            ////////////////////////
            miMenu.controlador.CrearDireccion(calle,  numero,  piso,  barrio,  localidad,  codigoPostal );
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error ("+ ex + ")");
        }

        try {
            //////////////////////////

            miMenu.controlador.CrearLocatario(usuario, password, tipoActividad, comprobante,  dni,  tipoDocumento,  apellido,  nombre,  estadoCivil,  telefono,  email,  fechaNacimiento);
         JOptionPane.showMessageDialog(null, "Agregado Correctamente");
        } catch (Exception ex) {
          JOptionPane.showMessageDialog(null, "Error ("+ ex + ")");
        }
        /////////////////////////Limpia los campos
            txt_dni.setText("");
            txt_nombre.setText("");
            txt_apellido.setText("");
            txt_fecha.setCalendar(null);
           txt_telefono.setText("");
            txt_email.setText("");
            txt_estadoCivil.setText("");
            txt_calle.setText("");
            txt_numero.setText("");
            txt_piso.setText("");
            txt_barrio.setText("");
            txt_localidad.setText("");
            txt_codigoPostal.setText("");
            txt_usuario.setText("");
            txt_password.setText("");
            txt_imagen.setText("");
         }

    }//GEN-LAST:event_btn_agregar1ActionPerformed

