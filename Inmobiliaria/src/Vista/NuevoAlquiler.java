    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
         Date fechaInicio = new Date ();
         Date fechaFinal = new Date ();
            if ((txt_dniGarante.getText().isEmpty() || txt_nombreGarante.getText().isEmpty() || txt_dniLocador.getText().isEmpty() || txt_nombreLocador.getText().isEmpty() || txt_dniLocatario.getText().isEmpty()  || txt_nombreLocatario.getText().isEmpty()  || txt_inmueble.getText().isEmpty() )
              ||( txt_direccionInmueble.getText().isEmpty() || txt_montoAlquiler.getText().isEmpty() || txt_recargoProporcional.getText().isEmpty() || txt_montoComision.getText().isEmpty()  || txt_montoAranceles.getText().isEmpty() || txt_montoRecargoFijo.getText().isEmpty() )){    
                      JOptionPane.showMessageDialog(null, "Todos los campos son OBLIGATORIOS");
         }else{
        int dniGarante = parseInt(txt_dniGarante.getText());
        int dniLocador = parseInt(txt_dniLocador.getText());
        int dniLocatario = parseInt(txt_dniLocatario.getText());
        String nombreLocatario = txt_nombreLocatario.getText();
        int idInmueble =parseInt(txt_inmueble.getText());
        double montoAlquiler =  Double.parseDouble(txt_montoAlquiler.getText());
        double recargoProporcional = Double.parseDouble(txt_recargoProporcional.getText());
        double montoComision = Double.parseDouble(txt_montoComision.getText());
        int tipoPagoComision = cmb_tipoPago.getSelectedIndex();
        double montoAranceles = Double.parseDouble(txt_montoAranceles.getText());
        double recargoFijo = Double.parseDouble(txt_montoRecargoFijo.getText());
                 try {
                fechaInicio = sdf.parse(txt_fechaActual.getText());
                fechaFinal =sdf.parse(txt_fechaFinalizacion.getText());
            } catch (ParseException ex) {
                Logger.getLogger(NuevoAlquiler.class.getName()).log(Level.SEVERE, null, ex);
            }
             int fechaPago1 = cmb_primerFechaPago.getSelectedIndex();
             if (fechaPago1 == 0){
                 fechaPago1= 7;
             }if (fechaPago1 == 1){
                 fechaPago1 = 10;
             }
             int fechaPago2 = cmb_segundaFechaPago.getSelectedIndex();
              if (fechaPago2 ==0){
                 fechaPago2= 14;
             }if (fechaPago2 == 1){
                 fechaPago2 = 17;
             }
             int duracion = 0;
        switch (cmb_duracion.getSelectedIndex()){
            case 0:  duracion = 18;
                break;
            case 1 : duracion = 24;
                break;
            case 2:  duracion = 30;
                  break;             
                               }
            
             try {
                 miMenu.controlador.CrearAlquiler (dniGarante, dniLocador, dniLocatario, nombreLocatario, idInmueble, montoAlquiler, recargoProporcional, montoComision,
                         tipoPagoComision, montoAranceles,recargoFijo, fechaInicio , fechaFinal ,fechaPago1,fechaPago2, duracion);
                  JOptionPane.showMessageDialog(null, "Creado Correctamente");
             } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error ("+ ex + ")");
             }
             //////////Limpiar campos
    txt_direccionInmueble.setText("");
    txt_dniGarante.setText("");
   txt_dniLocador.setText("");
     txt_dniLocatario.setText("");
     txt_fechaFinalizacion.setText("");
     txt_inmueble.setText("");
   txt_montoAlquiler.setText("");
   txt_montoAranceles.setText("");
    txt_montoComision.setText("");
   txt_montoRecargoFijo.setText("");
     txt_nombreGarante.setText("");
     txt_nombreLocador.setText("");
    txt_nombreLocatario.setText("");
   txt_recargoProporcional.setText("");
    txt_tipoInmueble.setText("");
     
      
        
        }
        
    }//GEN-LAST:event_btn_crearActionPerformed

